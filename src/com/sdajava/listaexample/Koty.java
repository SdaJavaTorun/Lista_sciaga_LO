public class Koty {

    public String test;
    public String cos;

    public Koty(String test, String cos) {
        this.test = test;
        this.cos = cos;
    }

    public String getTest() {
        return test;
    }

    public void setTest(String test) {
        this.test = test;
    }

    public String getCos() {
        return cos;
    }

    public void setCos(String cos) {
        this.cos = cos;
    }
}
